class YtMuxedStreamOption {
  /// 视频流 URL。muxed 时这是合并流(含音);split 时这是 video-only,
  /// 需配合 [audioUrl] 通过 `player.setAudioTrack(AudioTrack.uri(audioUrl))` 注入音频
  final String url;
  /// 高画质所需的外部音频流 URL。muxed 流时为 null
  final String? audioUrl;
  final String qualityLabel; // e.g. "720p"
  final int bitrate;
  const YtMuxedStreamOption({
    required this.url,
    required this.qualityLabel,
    required this.bitrate,
    this.audioUrl,
  });

  bool get isSplit => audioUrl != null;
}

class YtSubtitleTrackInfo {
  /// 在 UI 显示的语言名(如"中文"、"English"、"自动生成 中文")
  final String label;
  /// VTT 格式字幕 URL(YT timedtext API,已 append `&fmt=vtt`)
  final String url;
  final String langCode; // e.g. "zh-Hans", "en"
  final bool isAutoGenerated;
  const YtSubtitleTrackInfo({
    required this.label,
    required this.url,
    required this.langCode,
    required this.isAutoGenerated,
  });
}

class YtVideoService {
  /// 拿视频元信息
  Future<YtVideoDetail> fetchDetail(String videoId) async {
    final yt = YoutubeExplode();
    try {
      final v = await yt.videos.get(VideoId(videoId));
      return YtVideoDetail(
        videoId: v.id.value,
        title: v.title,
        author: v.author,
        duration: v.duration,
        viewCount: v.engagement.viewCount,
        likeCount: v.engagement.likeCount,
        description: v.description,
        thumbnailUrl: v.thumbnails.highResUrl,
        uploadDate: v.uploadDate,
        keywords: v.keywords.toList(),
      );
    } finally {
      yt.close();
    }
  }

  /// 拿 muxed 流 URL。返回 null 时表示无可用 muxed 流。
  /// 选最高 bitrate 的 muxed 流。
  Future<String?> fetchMuxedUrl(String videoId) async {
    final streams = await fetchMuxedStreams(videoId);
    if (streams.isEmpty) return null;
    return streams.first.url;
  }

  /// 拿全部 muxed 流(按 bitrate 降序),用于画质切换 UI
  static String _qualityLabel(String enumName) {
    // youtube_explode_dart 的 VideoQuality 枚举名形如 "high720" / "medium360"
    final m = RegExp(r'\d+').firstMatch(enumName);
    return m != null ? '${m.group(0)}p' : enumName;
  }

  /// 拿全部 native 字幕轨道列表(不预生成翻译选项,翻译留给 UI 层动态拼)。
  /// 每条 track 保留原始 URL,需要翻译时拼 `&tlang=lang` 参数。
  Future<List<YtSubtitleTrackInfo>> fetchSubtitleTracks(String videoId) async {
    final yt = YoutubeExplode();
    try {
      final manifest =
          await yt.videos.closedCaptions.getManifest(VideoId(videoId));
      final out = <YtSubtitleTrackInfo>[];
      for (final t in manifest.tracks) {
        out.add(YtSubtitleTrackInfo(
          label: t.isAutoGenerated
              ? '${t.language.name}(自动生成)'
              : t.language.name,
          url: _toVttUrl(t.url),
          langCode: t.language.code,
          isAutoGenerated: t.isAutoGenerated,
        ));
      }
      // 排序: 中文优先 → 非自动生成优先 → 字母序
      out.sort((a, b) {
        final aZh = a.langCode.startsWith('zh') ? 0 : 1;
        final bZh = b.langCode.startsWith('zh') ? 0 : 1;
        if (aZh != bZh) return aZh.compareTo(bZh);
        final aAuto = a.isAutoGenerated ? 1 : 0;
        final bAuto = b.isAutoGenerated ? 1 : 0;
        if (aAuto != bAuto) return aAuto.compareTo(bAuto);
        return a.label.compareTo(b.label);
      });
      return out;
    } catch (_) {
      return const [];
    } finally {
      yt.close();
    }
  }

  /// 把 native track URL 转成"自动翻译为 [tlang]"的 URL
  static String translateUrl(String nativeUrl, String tlang) {
    final uri = Uri.parse(nativeUrl);
    final params = Map<String, dynamic>.from(uri.queryParameters);
    params['tlang'] = tlang;
    return uri.replace(queryParameters: params).toString();
  }

