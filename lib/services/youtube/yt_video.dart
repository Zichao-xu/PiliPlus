  static String _toVttUrl(Uri original, {String? tlang}) {
    // YT timedtext API 默认返回 XML;append `&fmt=vtt` 让其返回 WebVTT
    final params = Map<String, dynamic>.from(original.queryParameters);
    params['fmt'] = 'vtt';
    if (tlang != null) params['tlang'] = tlang;
    return original.replace(queryParameters: params).toString();
  }

  /// 按当前最稳排序;yt_explode 内部 try-all fallback。
  /// 2026-05-19 dual-client 起步:androidVr / safari / mweb 优先(yt-dlp 推荐组合)
  static final _streamClients = [
    YoutubeApiClient.androidVr,
    YoutubeApiClient.safari,
    YoutubeApiClient.mweb,
    YoutubeApiClient.android,
    YoutubeApiClient.androidSdkless,
    YoutubeApiClient.ios,
